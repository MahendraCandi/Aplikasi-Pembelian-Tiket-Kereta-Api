    @Override
    public void bersih() {
        txtKode.setText("");
        txtNama.setText("");
        txtPassword.setText("");
    }

    @Override
    public void hapus() {
        try{
            uscont.delete(txtKode.getText());
            JOptionPane.showMessageDialog(null, "Data telah dihapus");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Data tidak nisa dihapus karena "+ex.getMessage());
        }
        bersih();
    }

    @Override
    public void cari() {
       usr=uscont.findUser(txtKode.getText());
        if(usr==null){
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
            txtNama.requestFocus();
        }else{
            JOptionPane.showMessageDialog(null, "Data ditemukan");
            tampil();
        }
    }

    @Override
    public void tampil() {
        txtNama.setText(usr.getNamaUser());
        txtPassword.setText(usr.getPassword());
    }
