    @Override
    public void aktif() {
        txtKode.setEnabled(true);
        txtNama.setEnabled(true);
        txtPassword.setEnabled(true);
        txtKode.requestFocus();
    }

    @Override
    public void simpan() {
        usr=uscont.findUser(txtKode.getText());
        User u=new User();
        if(usr==null){
            u.setKodeUser(txtKode.getText());
            u.setNamaUser(txtNama.getText());
            u.setPassword(txtPassword.getText());
            try{
                uscont.save(u);
            }catch(Exception e){}
            JOptionPane.showMessageDialog(null, "Data telah tersimpan");
        }else{
            u.setKodeUser(txtKode.getText());
            u.setNamaUser(txtNama.getText());
            u.setPassword(txtPassword.getText());
            try{
                uscont.update(u);
            }catch(Exception e){}
            JOptionPane.showMessageDialog(null, "Data telah terupdate");           
        }
        bersih();
        txtKode.requestFocus();
    }

